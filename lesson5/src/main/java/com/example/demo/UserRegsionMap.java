package com.example.demo;

public class UserRegsionMap {

	private String region;
	private String user;

	public UserRegsionMap(String region, String user) {
		this.region = region;
		this.user = user;
	}

	public String getRegion() {
		return region;
	}

	public void setRegion(String region) {
		this.region = region;
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}
}
